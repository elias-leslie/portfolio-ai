-- Migration 052: Link strategies to trades and positions
-- Enables tracking which strategy generated each trade/position
-- Required for: performance tracking, validation, manual trade linking

-- 1. Add strategy_id to idea_outcomes (paper trades)
ALTER TABLE idea_outcomes
ADD COLUMN IF NOT EXISTS strategy_id UUID;

-- Add foreign key constraint (SET NULL on delete to preserve trade history)
DO $$
BEGIN
    IF NOT EXISTS (
        SELECT 1 FROM information_schema.table_constraints
        WHERE constraint_name = 'fk_idea_outcomes_strategy'
    ) THEN
        ALTER TABLE idea_outcomes
        ADD CONSTRAINT fk_idea_outcomes_strategy
            FOREIGN KEY (strategy_id)
            REFERENCES strategy_definitions(id)
            ON DELETE SET NULL;
    END IF;
END $$;

-- Index for efficient strategy-based queries
CREATE INDEX IF NOT EXISTS idx_idea_outcomes_strategy_id
ON idea_outcomes(strategy_id);

-- Compound index for strategy + date queries (common in performance calcs)
CREATE INDEX IF NOT EXISTS idx_idea_outcomes_strategy_date
ON idea_outcomes(strategy_id, created_at DESC);

-- 2. Add strategy_id to portfolio_positions (manual trades)
