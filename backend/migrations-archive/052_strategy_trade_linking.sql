-- 4. Create strategy_signals table for daily signal storage
CREATE TABLE IF NOT EXISTS strategy_signals (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    strategy_id UUID NOT NULL REFERENCES strategy_definitions(id) ON DELETE CASCADE,
    symbol VARCHAR(10) NOT NULL,
    signal_date DATE NOT NULL,
    signal_type VARCHAR(10) NOT NULL CHECK (signal_type IN ('BUY', 'HOLD', 'SELL', 'AVOID')),
    signal_strength INT NOT NULL CHECK (signal_strength >= 0 AND signal_strength <= 10),
    reasons TEXT[],
    market_data JSONB,  -- Snapshot of data used to generate signal
    created_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT NOW(),

    -- One signal per strategy per day
    UNIQUE(strategy_id, signal_date)
);

-- Indexes for efficient queries
CREATE INDEX IF NOT EXISTS idx_strategy_signals_strategy_date
ON strategy_signals(strategy_id, signal_date DESC);

CREATE INDEX IF NOT EXISTS idx_strategy_signals_date
ON strategy_signals(signal_date DESC);

CREATE INDEX IF NOT EXISTS idx_strategy_signals_type
ON strategy_signals(signal_type, signal_date DESC);

-- 5. Update table_registry
INSERT INTO table_registry (table_name, table_type, description)
VALUES
    ('strategy_signals', 'data', 'Daily trading signals generated by active strategies')
ON CONFLICT (table_name) DO UPDATE SET
    description = EXCLUDED.description;
